use crate::aoc_2021::day_24_part_1::get_model_num_ranges;

#[allow(dead_code)]
fn day_24_part_2(input: &str) -> i64 {
    let model_num_ranges = get_model_num_ranges(input);

    let mut model_num: i64 = 0;
    for model_num_digit_range in model_num_ranges.iter() {
        model_num *= 10;
        model_num += model_num_digit_range[0];
    }

    return model_num;
}

