#[allow(dead_code)]
pub fn day_3_part_1(start_x: i32, start_y: i32, slope_x: i32, slope_y: i32, map: &str) -> usize {
    let num_rows: i32 = (map.chars().filter(|c| *c == '\n').count() + 1) as i32;
    let num_cols: i32 = map.find('\n').unwrap() as i32;

    let mut x = start_x;
    let mut y = start_y;
    let mut tree_count: usize = 0;

    while y < num_rows {
        let ch = map.chars().nth((y * (num_cols + 1) + x) as usize).unwrap();

        match ch {
            '#' => tree_count += 1,
            _ => ()
        }

        x += slope_x;
        x %= num_cols;
        y += slope_y;
    }
    
    return tree_count;
}

